def main():
    script_dir = os.path.dirname(os.path.abspath(__file__))
    headers = {
        'User-Agent': 'Mozilla/5.0 (Windows NT 10.0; Win64; x64) AppleWebKit/537.36 (KHTML, like Gecko) Chrome/120.0.0.0 Safari/537.36'
    }
    
    print("=" * 60)
    print("Sitara Story Narration Audio Generator (Free Fallback Endpoint)")
    print("=" * 60)
    
    success_count = 0
    fail_count = 0
    
    for story_idx, story_pages in enumerate(STORIES):
        print(f"\n--- Story {story_idx} ---")
        for page_idx, urdu_text in enumerate(story_pages):
            filename = f"story_{story_idx}_page_{page_idx}.mp3"
            filepath = os.path.join(script_dir, filename)
            
            # Encode Urdu text for query param
            encoded_text = urllib.parse.quote(urdu_text)
            url = f"https://translate.google.com/translate_tts?ie=UTF-8&tl=ur&client=tw-ob&q={encoded_text}"
            
            print(f"Generating audio for Page {page_idx}...")
            
            try:
                # Add delay to be gentle to Google's translation endpoint
                time.sleep(1.0)
                
                resp = requests.get(url, headers=headers, timeout=15)
                if resp.status_code == 200:
                    with open(filepath, 'wb') as f:
                        f.write(resp.content)
                    print(f"  [OK] Saved {len(resp.content)} bytes to {filename}")
                    success_count += 1
                else:
                    print(f"  [FAILED] HTTP status {resp.status_code}: {resp.text[:100]}")
                    fail_count += 1
            except Exception as e:
                print(f"  [ERROR] {e}")
                fail_count += 1
                
    print("\n" + "=" * 60)
    print(f"Story audio generation complete! Success: {success_count}, Failed: {fail_count}")
    print("=" * 60)

if __name__ == "__main__":
    main()
